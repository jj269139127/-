package Model;

public class ProjectBean {
	int Project_id;
	String Project_name;
	int Project_number;
	String Project_category;
	String Project_sex;
	public int getProject_id() {
		return Project_id;
	}
	public void setProject_id(int project_id) {
		Project_id = project_id;
	}
	public String getProject_name() {
		return Project_name;
	}
	public void setProject_name(String project_name) {
		Project_name = project_name;
	}
	public int getProject_number() {
		return Project_number;
	}
	public void setProject_number(int project_number) {
		Project_number = project_number;
	}
	public String getProject_category() {
		return Project_category;
	}
	public void setProject_category(String project_category) {
		Project_category = project_category;
	}
	public String getProject_sex() {
		return Project_sex;
	}
	public void setProject_sex(String project_sex) {
		Project_sex = project_sex;
	}
	

}
