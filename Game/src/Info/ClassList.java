package Info;

public class ClassList {
	private int class_id;
	private String class_name;
	private int class_score;
	private String class_department;
	public int getClass_id() {
		return class_id;
	}
	public void setClass_id(int classId) {
		class_id = classId;
	}
	public String getClass_name() {
		return class_name;
	}
	public void setClass_name(String className) {
		class_name = className;
	}
	public int getClass_score() {
		return class_score;
	}
	public void setClass_score(int classScore) {
		class_score = classScore;
	}
	public String getClass_department() {
		return class_department;
	}
	public void setClass_department(String classDepartment) {
		class_department = classDepartment;
	}
	
	
}
