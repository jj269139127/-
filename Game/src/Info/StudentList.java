package Info;

public class StudentList {
	private int competer_id;
	private int student_username;
	private String student_name;
	private String class_id;
	private String student_sex;
	private String student_introduction;
	public int getCompeter_id() {
		return competer_id;
	}
	public void setCompeter_id(int competerId) {
		competer_id = competerId;
	}
	public int getStudent_username() {
		return student_username;
	}
	public void setStudent_username(int studentUsername) {
		student_username = studentUsername;
	}
	public String getStudent_name() {
		return student_name;
	}
	public void setStudent_name(String studentName) {
		student_name = studentName;
	}
	public String getClass_id() {
		return class_id;
	}
	public void setClass_id(String classId) {
		class_id = classId;
	}
	public String getStudent_sex() {
		return student_sex;
	}
	public void setStudent_sex(String studentSex) {
		student_sex = studentSex;
	}
	public String getStudent_introduction() {
		return student_introduction;
	}
	public void setStudent_introduction(String studentIntroduction) {
		student_introduction = studentIntroduction;
	}
	
}
